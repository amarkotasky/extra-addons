#!/bin/bash

###################################################################################################
######                               Set enviornment variables
###################################################################################################
chmod -R 777 environment.sh
. environment.sh

echo ". environment.sh" >> ~/.bashrc \
&& . ~/.bashrc

echo $INT_CDN_URI
###################################################################################################
######                             Check Node & Npm Version
###################################################################################################

node -v
npm --version

###################################################################################################
######                                Starting Node Server
###################################################################################################
echo "Starting Server"
nodemon --trace-warnings server.js
